    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        Connection con = null;
        try {
            con = Conexao();
            ps = con.prepareStatement("select * from produto where codbarra like ?");
            ps.setString(1, txtBuscar.getText());
            rs = ps.executeQuery();
            if (rs.next()) {
                txtCodigo.setText(rs.getString("idproduto"));
                txtNome.setText(rs.getString("nome"));
                txtCodBarra.setText(rs.getString("codbarra"));
                txtPreco.setText(rs.getString("preco"));
                txtQuantidade.setText(rs.getString("quantidade"));
            } else {
                JOptionPane.showMessageDialog(this, "Não existe um produto com este código de Barra");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao Selecionar o Produto: " + e);
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        Connection con = null;

        if (txtCodigo.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Deve Inserir um código de barra para Editar os Dados");

        } else {
            try {
                con = Conexao();
                ps = con.prepareStatement("update produto set nome=?, codbarra=?, preco=?, quantidade=? where idproduto=?");
                ps.setString(1, txtNome.getText());
                ps.setString(2, txtCodBarra.getText());
                ps.setDouble(3, Double.parseDouble(txtPreco.getText()));
                ps.setInt(4, Integer.parseInt(txtQuantidade.getText()));
                ps.setString(5, txtCodigo.getText());

                int res = ps.executeUpdate();
                if (res > 0) {
                    JOptionPane.showMessageDialog(this, "Produto Alterado com Sucesso");
                    Limpar();
                } else {
                    JOptionPane.showMessageDialog(this, "Produto Não Alterado com Sucesso");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Erro ao Alterar o Produto: " + e);
            }
        }


    }//GEN-LAST:event_btnEditarActionPerformed

