    private void btnSalarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalarActionPerformed
        Connection con = null;
        try {
            con = Conexao();
            ps = con.prepareStatement("insert into produto (nome, codbarra, preco, quantidade) values (?,?,?,?)");
            ps.setString(1, txtNome.getText());
            ps.setString(2, txtCodBarra.getText());
            ps.setDouble(3, Double.parseDouble(txtPreco.getText()));
            ps.setInt(4, Integer.parseInt(txtQuantidade.getText()));

            int res = ps.executeUpdate();
            if (res > 0) {
                JOptionPane.showMessageDialog(this, "Produto Cadastrado com Sucesso");
                Limpar();
            } else {
                JOptionPane.showMessageDialog(this, "Produto Não Cadastrado Cadastrado com Sucesso");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao Inserir o Produto: " + e);
        }
    }//GEN-LAST:event_btnSalarActionPerformed

