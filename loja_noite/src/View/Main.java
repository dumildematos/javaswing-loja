    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        if(produtoPesquisa.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Existem campos que devem ser preenchidos");
        }else{
            ProdutoModel produto = new ProdutoModel();
            ProdutoController controller = new ProdutoController();
            controller.Pesquisar(produto, String.valueOf(produtoPesquisa.getText()));
            produtoNome.setText(produto.getNome());
            produtoCodigo.setText(produto.getCodigo());
            produtoPreco.setText(String.valueOf(produto.getPreco()));
            produtoQuantidade.setText(String.valueOf(produto.getQuantidade()));
            produtoIDLB.setText(String.valueOf(produto.getId()));
        }
    }//GEN-LAST:event_jButton5ActionPerformed

