    private void limpar() {
        if(jPProduto.isVisible()){
            produtoNome.setText("");
            produtoCodigo.setText("");
            produtoPreco.setText("");
            produtoQuantidade.setText(null);
        }
    }
    
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here: Adicionar Produto
        
        if(produtoNome.getText().isEmpty() || produtoCodigo.getText().isEmpty() || produtoPreco.getText().isEmpty() || produtoQuantidade.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Existem campos que devem ser preenchidos");
        }else{
           
            ProdutoModel produto = new ProdutoModel();
            ProdutoController controller = new ProdutoController();
            
            produto.setNome(produtoNome.getText());
            produto.setCodigo(produtoCodigo.getText());
            produto.setPreco(Float.valueOf(produtoPreco.getText()));
            produto.setQuantidade(Integer.valueOf(produtoQuantidade.getText()));
            
            controller.Adicionar(produto);
            limpar();
            
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

